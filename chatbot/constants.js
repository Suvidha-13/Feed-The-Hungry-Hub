function getBotResponse(userInput) {
  for (let i = 0; i < prompts.length; i++) {
    for (let j = 0; j < prompts[i].length; j++) {
      if (userInput.includes(prompts[i][j])) {
        // Check if the prompt corresponds to a dynamic response
        const dynamicResponse = getDynamicResponse(prompts[i][j]);
        // Return the corresponding dynamic response if available
        return dynamicResponse.length > 0 ? dynamicResponse : replies[i];
      }
    }
  }
  // If no match is found, return an alternative response
  return alternative;
}

